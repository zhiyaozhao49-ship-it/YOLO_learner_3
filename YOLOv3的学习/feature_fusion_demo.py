"""
YOLOv3 特征融合（Feature Fusion）小实验
=========================================

功能：模拟 YOLOv3 FPN 式特征融合的关键步骤 ——
      深层特征图上采样后与浅层特征图在通道维度拼接。
      不包含骨干网络、不训练，纯张量操作演示。

YOLOv3 的特征融合路径（自顶向下，类似 FPN）:

    骨干输出:
      feat_52 (52×52, 浅层)  ──────────────────────→ concat ←── upsample(feat_26_fused)
      feat_26 (26×26, 中层)  ──────→ concat ←── upsample(feat_13)     │
      feat_13 (13×13, 深层)  ──→ 检测头 3                         检测头 2
                                  │
                                  ↓ upsample + concat(feat_26)
                               检测头 1

    本脚本演示其中一个融合节点:
      feat_13 (深层) → upsample → concat(feat_26 浅层) → fused

参考：Lin et al., "Feature Pyramid Networks" (CVPR 2017)
      YOLOv3 论文 —— "An Incremental Improvement" (2018)
"""

import torch
import torch.nn as nn


def main():
    torch.manual_seed(42)

    print("=" * 70)
    print("  YOLOv3 特征融合（Feature Fusion）实验")
    print("=" * 70)

    # ═══════════════════════════════════════════════════════════════════════
    # Step 1: 模拟深层特征图 —— 来自 Darknet-53 最深层的输出
    # ═══════════════════════════════════════════════════════════════════════
    #
    # 深层特征图特点:
    #   · 空间分辨率低 (13×13)，但通道数多 (512)
    #   · 感受野大，看到的是全局语义信息（"这里有一只猫"）
    #   · 对小目标的定位能力差（细节在多次下采样中丢失了）

    deep_feat = torch.randn(1, 512, 13, 13)
    print(f"\n  [Step 1] 深层特征图 (来自 Darknet-53 末端)")
    print(f"           shape: {list(deep_feat.shape)}")
    print(f"           空间: 13×13, 通道: 512")
    print(f"           特点: 语义丰富但分辨率低")

    # ═══════════════════════════════════════════════════════════════════════
    # Step 2: 上采样 —— 把深层特征图放大 2 倍 (13×13 → 26×26)
    # ═══════════════════════════════════════════════════════════════════════
    #
    # YOLOv3 使用最近邻上采样 (nearest neighbor)，不使用转置卷积。
    # 为什么用最近邻而不是双线性插值或转置卷积？
    #   · 最近邻没有可学习参数，计算开销极小
    #   · 不会引入棋盘效应（转置卷积的常见问题）
    #   · 后续卷积层会负责"细化"上采样后的粗糙结果

    upsample = nn.Upsample(scale_factor=2, mode="nearest")
    deep_up = upsample(deep_feat)

    print(f"\n  [Step 2] 上采样 (nearest, 2×)")
    print(f"           {list(deep_feat.shape)} → {list(deep_up.shape)}")
    print(f"           13×13 放大 2 倍 → 26×26, 通道数不变")

    # ═══════════════════════════════════════════════════════════════════════
    # Step 3: 模拟浅层特征图 —— 来自 Darknet-53 中间层
    # ═══════════════════════════════════════════════════════════════════════
    #
    # 浅层特征图特点:
    #   · 空间分辨率高 (26×26)，通道数较少 (256)
    #   · 感受野小，看到的是局部细节（边缘、纹理、精确位置）
    #   · 对"这里是什么"的回答不够好，但"它在哪里"回答得很准

    shallow_feat = torch.randn(1, 256, 26, 26)
    print(f"\n  [Step 3] 浅层特征图 (来自 Darknet-53 中间层)")
    print(f"           shape: {list(shallow_feat.shape)}")
    print(f"           空间: 26×26, 通道: 256")
    print(f"           特点: 分辨率高但语义信息少")

    # ═══════════════════════════════════════════════════════════════════════
    # Step 4: 通道拼接 (Concatenate) —— 融合的关键操作
    # ═══════════════════════════════════════════════════════════════════════
    #
    # torch.cat 在 dim=1（通道维度）上拼接:
    #   深层 (512 通道) + 浅层 (256 通道) = 融合后 (768 通道)
    #
    # 为什么用 concat 而不是逐元素相加（add）？
    #   · add 要求通道数相同，会丢失部分信息
    #   · concat 保留了两个来源的全部通道，后续卷积层自行学习
    #     "哪些通道有用、怎么组合"
    #   · FPN 原论文用 add（因为 ResNet 各阶段通道一致），
    #     YOLOv3 用 concat（Darknet-53 各阶段通道不同）

    fused = torch.cat([deep_up, shallow_feat], dim=1)

    print(f"\n  [Step 4] 通道拼接 (dim=1)")
    print(f"           深层: {list(deep_up.shape)}")
    print(f"           浅层: {list(shallow_feat.shape)}")
    print(f"           融合: {list(fused.shape)}")
    print(f"           通道数: 512 + 256 = {fused.shape[1]}")

    # ═══════════════════════════════════════════════════════════════════════
    # Step 5: 后续卷积 —— 从融合后的 768 通道中提取有效信息
    # ═══════════════════════════════════════════════════════════════════════
    #
    # 在实际 YOLOv3 中，拼接后会接 5 个卷积层（1×1 压缩 + 3×3 扩张 × 2 + 输出）
    # 这里用一个 1×1 卷积演示通道降维

    compress = nn.Conv2d(768, 256, kernel_size=1, bias=False)
    compressed = compress(fused)

    print(f"\n  [Step 5] 1×1 卷积压缩通道 (实际 YOLOv3 会接更多卷积层)")
    print(f"           {list(fused.shape)} → {list(compressed.shape)}")
    print(f"           768 → 256, 为后续检测头做准备")

    # ═══════════════════════════════════════════════════════════════════════
    # 总结
    # ═══════════════════════════════════════════════════════════════════════

    print("\n" + "=" * 70)
    print("  为什么特征融合能帮助 YOLOv3 检测中小目标？")
    print("=" * 70)
    print("""
  核心矛盾:
    深层特征 → 知道「是什么」（语义强）但不知道「在哪里」（分辨率低）
    浅层特征 → 知道「在哪里」（分辨率高）但不知道「是什么」（语义弱）

  YOLOv2 的问题:
    只在 13×13 深层特征图上检测，小目标的细节已经在多次下采样中丢失，
    即使网络"知道"那里有物体，也无法精确定位 —— 导致小目标漏检。

  YOLOv3 的解决方案 —— FPN 式特征融合:
    把深层的语义信息"向上传递"，与浅层的高分辨率细节融合:
      深层 13×13 → 上采样 → concat(浅层 26×26) → 26×26 融合特征图
      融合特征图既有深层语义（知道是什么），又有浅层细节（知道在哪里）

  三个尺度的融合路径（简化版）:

      深层 feat_13 ──→ 检测头 3 (大目标)
            │
            ↓ upsample(2×)
            ├─ concat(feat_26 浅层) ──→ 检测头 2 (中目标)
            │         │
            │         ↓ upsample(2×)
            │         ├─ concat(feat_52 浅层) ──→ 检测头 1 (小目标)
            │         │
            ↑         ↑
          深层语义   浅层细节

    13×13 的语义信息被逐层"传递"到 26×26 和 52×52，
    使得小尺度特征图也能拥有较强的物体识别能力。
  """)


if __name__ == "__main__":
    main()
